
Hw = "Hello, World!"
print(Hw)

# characters in Hw
print(len(Hw))

# first character
print(Hw[0])

# last character
print(Hw[-1])

# second last character
print(Hw[-2])

# slices between 3rd character and end
print(Hw[2:])

# slices between third character from end and end
print(Hw[-3:])

# slices between start and 5th character
print(Hw[:5])

# starts from the second, stops at the fifth (doesn't include it)
print(Hw[1:4])